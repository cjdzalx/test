# 断言
# assertEqual 判断两个值是否相等
# assertNotEqual 判断两个值是否不等
# assertListEqual 判断两个list是否相等
# assertTupleEqual 判断两个元祖是否相等
# assertSetEqual 判断两个集合是否相等
# assertIn 判断a是否在b中
# assertNotIn 判断a不在b中
# assertIs 判断a等于b  is 是
# assertIsNot 判断a不等于b
# assertDictEqual 判断两个字典是否相等
# assertCountEqual 数值
# assertIsNone 为空
# assertIsNotNone  不为空
# assertIsInstance  类型相等
# assertNotIsInstance  类型不等